async function getLatestProjects() {
  try {
    const payload = await getPayload({ config });
    const result = await payload.find({
      collection: "projects",
      limit: 4,
      sort: ["displayOrder", "-createdAt"],
      depth: 1,
    });
    return result.docs || [];
  } catch (error) {
    console.error("Error fetching latest projects:", error);
    return [];
  }
}

